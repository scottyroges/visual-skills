import { describe, it, expect } from "vitest";
import { assemble } from "../src/assemble.js";
import type { Block } from "../src/blocks.js";

describe("assemble", () => {
  it("produces one self-contained HTML doc with inlined CSS, header, and rendered blocks", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "p", markdown: "Intro **text**." },
      { type: "diagram", id: "flow", title: "Flow", kind: "flowchart", d2: "a -> b" },
    ];
    const html = await assemble(blocks, {
      title: "Test Plan", source: "spec.md", status: { level: "green", text: "ready" },
    });
    expect(html).toMatch(/^<!doctype html>/i);
    expect(html).toContain("<style>");
    expect(html).not.toContain("<link");
    expect(html).toContain("Test Plan");
    expect(html).toContain("spec.md");
    expect(html).toContain('class="vs-status green"');
    expect(html).toContain("<strong>text</strong>");
    expect(html).toContain("<svg");
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i); // only the inlined viewer, never external
  });

  it("renders annotated-code and questions blocks without throwing", async () => {
    const blocks: Block[] = [
      {
        type: "annotated-code", id: "ac", title: "flow", lang: "ts",
        code: "const a = 1;\nconst b = 2;",
        annotations: [{ line: 1, note: "first" }],
      },
      {
        type: "questions", id: "q", title: "Open questions",
        questions: [{ question: "Ship it?", recommendedDefault: "yes" }],
      },
    ];
    const html = await assemble(blocks, { title: "All Blocks", source: "spec.md" });
    expect(html).toContain('class="vs-block vs-annotated"');
    expect(html).toContain('class="vs-block vs-questions"');
    expect(html).toContain("first");
    expect(html).toContain("Ship it?");
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i); // only the inlined viewer, never external
  });

  it("renders a generator stamp in a footer and meta tag when provided", async () => {
    const html = await assemble([{ type: "prose", id: "p", markdown: "hi" }], {
      title: "T", source: "s", generator: "visual-skills@abc123 · 2026-06-17T23:10:00Z",
    });
    expect(html).toContain('<meta name="generator" content="visual-skills@abc123 · 2026-06-17T23:10:00Z">');
    expect(html).toContain('class="vs-generator"');
    expect(html).toContain("Generated by visual-skills@abc123");
  });

  it("omits the generator footer/meta when not provided", async () => {
    const html = await assemble([{ type: "prose", id: "p", markdown: "hi" }], { title: "T", source: "s" });
    expect(html).not.toContain('name="generator"');
    // The .vs-generator CSS rule is always inlined; assert the footer ELEMENT is absent.
    expect(html).not.toContain('<footer class="vs-generator"');
  });

  it("renders a group as an open collapsible details with a markdown description", async () => {
    const blocks: Block[] = [
      { type: "group", id: "g1", title: "Core change", description: "The **heart** of it.",
        blocks: [
          { type: "diff", id: "diff-0", title: "x.ts", path: "src/x.ts",
            hunks: [{ header: "@@", lines: ["+a"] }] },
        ] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain('class="vs-block vs-group"');
    expect(html).toMatch(/<details[^>]*\bopen\b/);        // groups start open
    expect(html).toContain('class="vs-group-desc"');
    expect(html).toContain("<strong>heart</strong>");      // description is markdown
    expect(html).toContain('id="g1"');                     // anchor preserved
  }, 30_000);

  it("renders a group with nested blocks, anchors, and a nested diagram", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "summary", title: "Summary", markdown: "hi" },
      { type: "group", id: "g1", title: "Core change", blocks: [
        { type: "diagram", id: "flow", title: "Flow", kind: "flowchart", d2: "a -> b" },
        { type: "diff", id: "diff-0", title: "x.ts", path: "src/x.ts", hunks: [{ header: "@@", lines: ["+a"] }] },
      ] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain('class="vs-block vs-group"');
    expect(html).toContain("Core change");
    expect(html).toContain('id="g1"');     // group anchor
    expect(html).toContain('id="flow"');   // nested diagram anchor
    expect(html).toContain('id="diff-0"'); // nested diff anchor
    expect(html).toContain("<svg");        // nested diagram actually rendered
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i); // only the inlined viewer, never external
  }, 30_000);

  it("stamps an id anchor on a top-level block section", async () => {
    const html = await assemble([{ type: "prose", id: "p1", markdown: "x" }], { title: "T", source: "s" });
    expect(html).toContain('id="p1"');
  });

  it("throws on a nested group (groups may not nest)", async () => {
    const blocks: Block[] = [
      { type: "group", id: "g1", title: "A", blocks: [{ type: "group", id: "g2", title: "B", blocks: [] }] },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/nest/);
  });

  it("throws on duplicate block ids (anchors/cross-links must be unique)", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "dup", markdown: "a" },
      { type: "prose", id: "dup", markdown: "b" },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/duplicate block id/);
  });

  it("links a file-tree entry to its diff block by path", async () => {
    const blocks: Block[] = [
      { type: "file-tree", id: "files", title: "Files changed",
        files: [{ path: "src/x.ts", status: "M", added: 1, deleted: 0 }] },
      { type: "diff", id: "diff-0", title: "x.ts", path: "src/x.ts",
        hunks: [{ header: "@@", lines: ["+a"] }] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain('href="#diff-0"');
  });

  it("throws on a duplicate id nested in a group", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "x", markdown: "a" },
      { type: "group", id: "g", title: "G", blocks: [{ type: "prose", id: "x", markdown: "b" }] },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/duplicate block id/);
  });

  it("renders a legend under a diagram that applies roles, and none for a plain diagram", async () => {
    const colored = await assemble(
      [{ type: "diagram", id: "c", title: "C", kind: "flowchart", d2: "x: { class: changed }" }],
      { title: "T", source: "s" });
    expect(colored).toContain('class="vs-legend"');
    expect(colored).toContain("Changed");

    const plain = await assemble(
      [{ type: "diagram", id: "p", title: "P", kind: "flowchart", d2: "a -> b" }],
      { title: "T", source: "s" });
    expect(plain).not.toContain('class="vs-legend"');
  }, 30_000);

  it("inlines exactly one self-contained viewer script (no external src) and wraps diagrams as zoomable", async () => {
    const blocks: Block[] = [
      { type: "diagram", id: "flow", title: "Flow", kind: "flowchart", d2: "a -> b" },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain("<script>");                 // the inlined viewer
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i);    // never an external script
    expect(html).toContain("vs-zoom-overlay");          // viewer code is present
    expect(html).toContain('class="vs-zoomable"');      // the diagram svg is wrapped
  }, 30_000);
});

describe("assemble — tabs block", () => {
  it("renders multiple panels with exactly one visible by default", async () => {
    const blocks: Block[] = [
      { type: "tabs", id: "views", title: "Two views", tabs: [
        { label: "Flow", block: { type: "diagram", id: "d-flow", title: "Flow", kind: "flowchart", d2: "a -> b" } },
        { label: "Seq", block: { type: "diagram", id: "d-seq", title: "Seq", kind: "sequence", d2: "shape: sequence_diagram\na -> b: hi" } },
      ] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain('class="vs-block vs-tabs"');
    expect(html).toContain('id="views"');                 // container anchor
    expect(html).toContain('name="vs-tabs-views"');        // radio group
    expect(html).toContain("checked");                     // first tab pre-selected
    expect(html).toContain(">Flow<");
    expect(html).toContain(">Seq<");
    // both diagrams rendered into panels
    expect((html.match(/<svg/g) ?? []).length).toBeGreaterThanOrEqual(2);
    // exactly one radio is checked by default
    expect((html.match(/type="radio"[^>]*\schecked/g) ?? []).length).toBe(1);
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i); // only the inlined viewer, never external
  });

  it("collects a diagram nested inside a tab for the up-front render pass", async () => {
    const blocks: Block[] = [
      { type: "tabs", id: "v", tabs: [
        { label: "A", block: { type: "diagram", id: "nested", title: "Nested", kind: "flowchart", d2: "a -> b" } },
      ] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain('id="nested"');   // got an anchor => was rendered, not skipped
    expect(html).toContain("<svg");
  });

  it("throws on a duplicate id nested inside a tab", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "dup", markdown: "x" },
      { type: "tabs", id: "v", tabs: [
        { label: "A", block: { type: "prose", id: "dup", markdown: "y" } },
      ] },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/duplicate block id "dup"/);
  });

  it("throws when a tab contains a group or tabs (one level deep)", async () => {
    const blocks: Block[] = [
      { type: "tabs", id: "v", tabs: [
        { label: "A", block: { type: "group", id: "g", title: "G", blocks: [] } },
      ] },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/may not contain a group or tabs/);
  });
});

describe("assemble — per-diff diagram (collection & uniqueness)", () => {
  it("collects a diagram embedded on a diff into the up-front render pass", async () => {
    // A broken d2 source only produces a warning if the embedded diagram was actually
    // collected and run through renderAll — so this proves collectDiagrams recursed into it.
    const warnings: string[] = [];
    const blocks: Block[] = [
      {
        type: "diff", id: "d0", title: "x.ts", path: "src/x.ts",
        diagram: { type: "diagram", id: "d0-diag", title: "Flow", kind: "flowchart", d2: "x: {" },
        hunks: [{ header: "@@", lines: ["+a"] }],
      },
    ];
    await assemble(blocks, { title: "T", source: "s", onWarn: (m) => warnings.push(m) });
    expect(warnings.some((w) => w.includes("d0-diag"))).toBe(true);
  }, 30_000);

  it("throws when a diff's embedded diagram id duplicates another block id", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "dup", markdown: "x" },
      {
        type: "diff", id: "d0", title: "x.ts", path: "src/x.ts",
        diagram: { type: "diagram", id: "dup", title: "Flow", kind: "flowchart", d2: "a -> b" },
        hunks: [{ header: "@@", lines: ["+a"] }],
      },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/duplicate block id "dup"/);
  });

  it("places a single embedded diagram's svg inside the vs-diff section", async () => {
    const blocks: Block[] = [
      {
        type: "diff", id: "d1", title: "x.ts", path: "src/x.ts",
        diagram: { type: "diagram", id: "d1-diag", title: "Flow", kind: "flowchart", d2: "a -> b" },
        hunks: [{ header: "@@", lines: ["+a"] }],
      },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    const sectionStart = html.indexOf('class="vs-block vs-diff"');
    const sectionEnd = html.indexOf("</section>", sectionStart);
    const section = html.slice(sectionStart, sectionEnd);
    expect(section).toContain("vs-diff-diagram");
    expect(section).toContain("<svg");
  });

  it("renders a tabs set embedded on a diff (multiple svgs, tab switcher)", async () => {
    const blocks: Block[] = [
      {
        type: "diff", id: "d2", title: "x.ts", path: "src/x.ts",
        diagram: {
          type: "tabs", id: "d2-views", title: "Views", tabs: [
            { label: "Flow", block: { type: "diagram", id: "d2-flow", title: "Flow", kind: "flowchart", d2: "a -> b" } },
            { label: "Seq", block: { type: "diagram", id: "d2-seq", title: "Seq", kind: "sequence", d2: "shape: sequence_diagram\na -> b: hi" } },
          ],
        },
        hunks: [{ header: "@@", lines: ["+a"] }],
      },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    expect(html).toContain("vs-diff-diagram");
    expect(html).toContain('class="vs-block vs-tabs"');
    expect((html.match(/<svg/g) ?? []).length).toBeGreaterThanOrEqual(2);
    expect(html).not.toMatch(/<script[^>]*\ssrc=/i); // only the inlined viewer, never external
  });
});

describe("assemble — overview block", () => {
  it("renders an overview with an embedded diagram and a resolvable point link", async () => {
    const blocks: Block[] = [
      { type: "overview", id: "ov", headline: "Lead", points: [{ text: "see the diff", href: "#diff-0" }],
        diagram: { type: "diagram", id: "ov-diag", title: "Flow", kind: "flowchart", d2: "a -> b" } },
      { type: "diff", id: "diff-0", title: "x.ts", path: "src/x.ts", hunks: [{ header: "@@", lines: ["+a"] }] },
    ];
    const html = await assemble(blocks, { title: "T", source: "s" });
    const start = html.indexOf('class="vs-block vs-overview"');
    const end = html.indexOf("</section>", start);
    const section = html.slice(start, end);
    expect(section).toContain("vs-overview-diagram");
    expect(section).toContain("<svg");
    expect(html).toContain('class="vs-point-link" href="#diff-0"'); // trailing arrow link
    expect(html).toContain('id="diff-0"'); // the link target exists
  });

  it("collects an overview's embedded diagram into the up-front pass (broken d2 warns)", async () => {
    const warnings: string[] = [];
    const blocks: Block[] = [
      { type: "overview", id: "ov", headline: "Lead", points: [],
        diagram: { type: "diagram", id: "ov-bad", title: "B", kind: "flowchart", d2: "x: {" } },
    ];
    await assemble(blocks, { title: "T", source: "s", onWarn: (m) => warnings.push(m) });
    expect(warnings.some((w) => w.includes("ov-bad"))).toBe(true);
  }, 30_000);

  it("throws when an overview's embedded diagram id duplicates another block id", async () => {
    const blocks: Block[] = [
      { type: "prose", id: "dup", markdown: "x" },
      { type: "overview", id: "ov", headline: "Lead", points: [],
        diagram: { type: "diagram", id: "dup", title: "B", kind: "flowchart", d2: "a -> b" } },
    ];
    await expect(assemble(blocks, { title: "T", source: "s" })).rejects.toThrow(/duplicate block id "dup"/);
  });
});
